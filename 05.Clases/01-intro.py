""" Una clase es una estructura que permite encapsular datos y funciones relacionadas
 en un solo objeto. Las clases son un componente fundamental de la programación orientada
 a objetos (OOP) en Python. En OOP, los objetos son instancias de clases, y cada
 objeto puede tener atributos (datos) y métodos (funciones) asociados. """


# En este caso, lo que has creado es una instancia de la clase str (cadena de texto) en Python.
# La variable mensaje contiene una cadena de texto, y al utilizar la función type(mensaje),
# estás obteniendo el tipo de la variable mensaje, que es str

# Si bien no se ha creado explícitamente una clase, se está utilizando un tipo de datos
# predefinido en Python que internamente es una clase. En Python, prácticamente todo
# es un objeto y tiene un tipo asociado.

mensaje = "Hola Mundo"
# Imprime <class 'str'>, que indica que 'mensaje' es de tipo cadena de texto (str)
print(type(mensaje))

# En este caso, <str> es la clase de las cadenas de texto en Python, y "Hola Mundo" es
# una instancia de esa clase. # La función type() te permite conocer el tipo de un objeto en Python.

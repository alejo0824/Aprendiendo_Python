# los comparadores lógicos se utilizan para combinar
# expresiones lógicas y producir un resultado booleano
# (True o False). Aquí están los principales

x = 3
y = 5

igual = x == y       # False
no_igual = x != y    # True
menor_que = x < y    # True
mayor_que = x > y    # False
menor_igual = x <= y  # True
mayor_igual = x >= y  # False

p = True
q = False

and_resultado = p and q   # False
or_resultado = p or q     # True
not_resultado = not p     # False

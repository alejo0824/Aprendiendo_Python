# math es una biblioteca estándar que proporciona funciones matemáticas y constantes
import math

# Round se utiliza para redondear un número decimal a la cantidad específica
print(round(1.3))  # Salida: 1
print(round(8.9876, 2))  # Salida: 8.99

# abs: se utiliza para obtener el valor absoluto de un número y
# el valor absoluto de un número siempre es un número posivo.
print(abs(-77))
print(abs(55))

# Usando el módulo math (Documentación https://docs.python.org/3/library/math.html)
print(math.ceil(1.1))  # Aproximar
print(math.ceil(1.9999))
print(math.isnan(23))  # Saber si es nu numéro
print(math.pow(10, 3))  # potencia
print(math.sqrt(9))  # Raíz cuadrada
